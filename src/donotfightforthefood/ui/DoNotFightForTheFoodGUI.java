    private void btCheckWinnerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCheckWinnerActionPerformed
        VotesDao votes = new VotesDao();
        
        String answer = JOptionPane.showInputDialog(rootPane, "This is a definitive action. When checking the winner, the voting will be finished by today. To confirm, please, write 'yes' in the text field bellow.");
        if(answer != null && answer.equalsIgnoreCase("yes")){
            String winner = votes.checkWinner();
            if(winner == null){
                JOptionPane.showMessageDialog(rootPane, "There are no votes registered yet.", "No votes registered", WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Prepare yourself (and your stomach). Today's winner is " + winner, "No votes registered", INFORMATION_MESSAGE);
            }
        }  
    }//GEN-LAST:event_btCheckWinnerActionPerformed

