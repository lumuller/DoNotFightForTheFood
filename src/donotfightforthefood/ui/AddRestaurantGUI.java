    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        String restName = txName.getText().toUpperCase();        
        double averPrice = Double.parseDouble(txPrice.getText());
        String foodType = txTypeFood.getText().toUpperCase();
        
        if(restaurants.addNewRestaurant(new Restaurant(restName,averPrice,foodType))){
            JOptionPane.showMessageDialog(rootPane, "Restaurant added successfully.","Success",INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error while adding new restaurant. This restaurant name is already registered.","Error",ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_btSaveActionPerformed

    
